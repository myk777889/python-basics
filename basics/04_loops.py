# Python Loops Example

# For loop
print("For Loop:")
for i in range(5):
    print("Number:", i)

print("\nWhile Loop:")
# While loop
counter = 0
while counter < 5:
    print("Counter:", counter)
    counter += 1

print("\nLoop Through a List:")
fruits = ["apple", "banana", "mango"]
for fruit in fruits:
    print(fruit)
